const toBlob = (dataURI: string) => {
    const b64 = dataURI.split(",")[1];
    const bin = atob(b64);
    const ia = new Uint8Array(bin.length);
    for (let i = 0; i < bin.length; i++) {
        ia[i] = bin.charCodeAt(i);
    }
    return ia;
};

